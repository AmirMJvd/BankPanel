package model;

public class Deposit {
    private String depostNumber;
    private String Inventory;
    private String shebaNumer;
    private String DepositType;



    public String getdepostNumber() {return depostNumber;}

    public void setdepostNumber(String depostNumber) {
        this.depostNumber = depostNumber;
    }

    public String getInventory() {return Inventory;}

    public void setInventory(String LastName) {this.Inventory = LastName;}

    public String getShebaNumer() {return shebaNumer;}

    public void setShebaNumer(String shebaNumer) {this.shebaNumer = shebaNumer;}

    public String getDepositType() {return DepositType;}

    public void setDepositType(String DepositType) {this.DepositType = DepositType;}
}
